package ex_11_While_Loop;

public class Lab107_while_loop {
    public static void main(String[] args) {
        int i = 0;  //Initialization
        while (i < 10){  //Condition
            System.out.println(i);
            i++; // updation

            // I / C / U
            for (int j = 0; j < 10; j++) {
                System.out.println(j);

            }
        }
    }
}
