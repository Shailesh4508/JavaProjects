package ex_10_For_Loop;

public class Lab096_For_Loop {
    public static void main(String[] args) {
        for (int thakur = 0; thakur < 18; thakur++){
            if (thakur > 15){
                System.out.println("Gift from Father: Bike");
            }
            else {
                System.out.println("No Gift from Father");
            }
        }
    }
}
