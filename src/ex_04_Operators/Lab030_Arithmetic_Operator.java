package ex_04_Operators;

public class Lab030_Arithmetic_Operator {
    public static void main(String[] args) {

        // Arithmetic Operators
        //  + (Addition)
        //  - (Subtraction)
        //  * (Multiplication)
        //  / (Division)
        //  % (Modulo / Modulus)

        int a = 20;
        int b = 3;
        float f = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/f);


    }
}
