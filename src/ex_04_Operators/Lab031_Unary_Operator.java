package ex_04_Operators;

public class Lab031_Unary_Operator {
    public static void main(String[] args) {

        //Unary Operator
        int a = +10;
        int b = -110;
        System.out.println(a);
        System.out.println(b);


        int c = -1;
        c = c+1;
        System.out.println(c);

    }
}
